clear;
A = [1,1,1;2,-1,-1;2,-4,5];
Q0 = [1,0,0;0,1,0;0,0,1];
for i = 1:1
    u = A(:,1)+[norm(A(:,1));0;0]
    u = u./norm(u)
    H1 = [1,0,0;0,1,0;0,0,1]-2*(u*u.')
    A = H1*A
    u = A(2:3,2)+[norm(A(2:3,2));0]
    u = u./norm(u)
    H2 = [1,0;0,1]-2*(u*u.');
    Z = [0 0];
    X = [0;0];
    H2 = [  1 Z;
            X H2]
    R = H2*A
    Q = H1.'*H2.'
    Q0 = Q0*H1.'*H2.'
    A = R*Q
end